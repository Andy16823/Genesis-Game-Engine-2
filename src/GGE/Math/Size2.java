public class Size2 {
    private int Width;
    private int Height;

    public Size2() {
    }

    public Size2(int width, int height) {
        Width = width;
        Height = height;
    }

    public int getWidth() {
        return Width;
    }

    public void setWidth(int width) {
        Width = width;
    }

    public int getHeight() {
        return Height;
    }

    public void setHeight(int height) {
        Height = height;
    }

}
